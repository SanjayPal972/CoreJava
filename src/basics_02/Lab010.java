package src.basics_02;

public class Lab010 {
    public static void main(String[] args) {
        final float pie = 410.10f;
        System.out.println(pie);

        // if use final keyword we can't change the value of pie
        System.out.println(pie);

        final String str = "TTA"; // Cannot assign the value if final keyword is used
        System.out.println(str);



    }
}
