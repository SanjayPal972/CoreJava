package src.basics_02;

public class Lab016 {
    public static void main(String[] args) {

        // Assignment Operator
        // =
        int a = 100;
        System.out.println(a);

        //Arithmetic Operator
        // +, -, /, *, %
        int num1 = 200;
        int num2 = 300;
        System.out.println(num1 + num2);

        String FN = "Sanjay";
        String LN = "Pal";
        System.out.println(FN + LN);

        //Unary operator
        int a1 = 10;
        int a2 = 10;
        System.out.println(a1);
        System.out.println(a2);

    }
}
