package src.basics_03_16thDec;

public class Lab023 {

    public static void main(String[] args) {

        // || - OR operator
        // True || True --T
        // T || F -- T
        // F || T -- T
        // F || F -- F

        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);

        // && -- AND -- operator

        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);
    }
}
